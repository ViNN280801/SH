#include <stdlib.h>

/* non-const types */
typedef int *pint;

typedef double *pdouble;
typedef double **ppdouble;

typedef unsigned int uint;
typedef unsigned int *puint;

typedef unsigned long long ullong;
/* End of non-const types */

/* const types */
typedef const double kdouble;
typedef const double *kpdouble;

typedef const int kint;
typedef const int *kpint;

typedef const unsigned int kuint;
typedef const unsigned int *kpuint;

typedef const size_t ksize_t;
/* End of const types */
